package config;

public interface GlobalConstants {


    /**
     * Defines standard chart cell width.
     * setting horizontal pixel distance
     * between consecutive data points.
     */
    public static final double MIN_CHART_CELL_WIDTH = 5d;

    /**
     * Defines standard candlestick width in pixels
     */
    public static final double MIN_CANDLESTICK_WIDTH = 3d;

    /**
     * Defines interline width in pixels
     */
    public static final double MIN_INTERLINE_WIDTH=0d;


    /**
     * Defines chart area padding in pixels
     */
    public static final double MIN_LEFT_PADDING=20d;
    public static final double MIN_RIGHT_PADDING=20d;
    public static final double MIN_TOP_PADDING=20d;
    public static final double MIN_BOTTOM_PADDING=20d;

    /**
     * Defines chart window minimum size in pixels
     */
    public static final double MIN_CHART_WIDTH=600d;
    public static final double MIN_CHART_HEIGHT=300d;




}
